// Declara variáveis
let numbers = [5, 9, 3, 19, 70, 8, 100, 2, 35, 27];

// Laço For -> Percorre o array imprimindo os valores
for (let index = 0; index < numbers.length; index += 1) {
    console.log(numbers[index]);
}
